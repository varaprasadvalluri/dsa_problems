package com.problems;

public class Address {

	private int id;
	private String ad1;

	public Address(int id, String ad1) {
		super();
		this.id = id;
		this.ad1 = ad1;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getAd1() {
		return ad1;
	}

	public void setAd1(String ad1) {
		this.ad1 = ad1;
	}

}
